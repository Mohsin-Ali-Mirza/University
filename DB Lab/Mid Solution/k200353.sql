CREATE TABLE CATEGORIES
(
    CATEGORY_ID INT,
    CATEGORY_NAME VARCHAR(20),
    CATEGORY_TYPE VARCHAR(20),
    
    CONSTRAINT CATEGORY_PK PRIMARY KEY (CATEGORY_ID)
);

